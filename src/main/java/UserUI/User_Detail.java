    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (isChanged(user)) {
            // Update the user data and create a new block
            String newData = userName.getText() + "|" +
                         fullName.getText() + "|" +
                         user.getPassword() + "|" +
                         gender.getText() + "|" +
                         DoB.getText() + "|" +
                         email.getText();
            // TODO: Create a new block with the updated data and add it to the blockchain
            BlockLogic bl = new BlockLogic();
            user.setUsername(userName.getText());
            user.setFullName(fullName.getText());
            user.setGender(gender.getText());
            user.setDoB(DoB.getText());
            user.setEmail(email.getText());
            bl.updateData(user.getID(), newData);
           
            JOptionPane.showMessageDialog(null, "Updated successfully!");
        } else {
            JOptionPane.showMessageDialog(null, "No changes detected.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

